public class FunSimple_01 {
    public static void main(String[] args) {
        PowerA3(3, 4, 5, 2.5, 8.6);
    }

    static void PowerA3(int a, int b, int c, double d, double e) {
        a = a * a * a; //bu nima
        b = b * b * b; // bu git hub
        c = c * c * c;
        d = d * d * d;
        e = e * e * e;
        System.out.println(a + " " + b + " " + " " + c + " " + d + " " + e);

    }

}